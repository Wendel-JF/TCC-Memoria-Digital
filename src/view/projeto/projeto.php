                        a partir da pluralidade de procedimentos epistemológicos, científicos e tecnológicos, de modo a compreender e posicionar-se criticamente
                        em relação a eles, considerando diferentes pontos de vista e tomando decisões baseadas em argumentos e fontes de natureza científica.
                    </li>
                    <li class="list-group-item bg-dark text-white">
                        • Além disso, o projeto também abarca eixos estruturantes dos Itinerários Formativos. O eixo da “Investigação Científica”, por exemplo, visa:

                        • Ampliar a capacidade dos estudantes de investigar a realidade, compreendendo, valorizando e aplicando o conhecimento sistematizado,
                        por meio da realização de práticas e produções científicas relativas a uma ou mais Áreas de Conhecimento, à Formação Técnica e Profissional,
                        bem como a temáticas de seu interesse.
                    </li>
                    <li class="list-group-item bg-dark text-white">
                        • Acerca da atualidade e da contribuição social do tema, destaca-se que os estudos das histórias dos escravos do interior de Pernambuco são
                        um fio condutor na (re)construção das identidades culturais dos atuais descendentes desses homens e mulheres escravizados no passado. Muitos
                        desses descendentes ainda continuam a residir nas regiões do interior de Pernambuco, onde outrora seus antepassados foram fixados forçadamente,
                        pouco conhecendo da história e cultura do seu povo.
                    </li>
