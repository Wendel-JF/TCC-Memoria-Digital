                <p >Na década passada, o sistema educacional foi incumbido de promover o conhecimento e valorização da contribuição africana e indígena para a sociedade brasileira, através da Lei 10.639 de 2003, seguida da Lei 11. 645, onde se tornou obrigatório o ensino da história e da cultura africana, afro-brasileira e indígena no currículo da educação básica. </p>
                <p > A partir da criação dessas leis, é inegável que houve o aumento da sensibilização e valorização sobre os referidos temas nos ensinos fundamental e médio das diversas escolas do país. Infelizmente, nos últimos anos, através da publicação da Medida Provisória 746/2016, o ensino da história e da cultura africana, afro-brasileira e indígena passou a ser facultativo, e não mais obrigatório. Contudo, apesar desse retrocesso, muitos educadores e escolas continuam sensibilizados a respeito da importância do tema, procurando desenvolver em suas instituições de ensino os mais diversos tipos de trabalhos.
                    A Escola Técnica Estadual Ministro Fernando Lyra (ETEMFL), em Pernambuco, é uma dessas instituições.</p>

                <p>     No intuito de trabalhar a história e cultura africana e afro-brasileira, a escola pretende se debruçar sobre um tema pouquíssimo investigado: a presença escrava no interior de Pernambuco (agreste e sertão), durante os séculos XVIII e XIX.</p>

                <p> Os escravos do interior de Pernambuco eram homens e mulheres, velhos, jovens, crianças e até bebês. Raros eram os que tinham algum sobrenome. A maior parte mesmo eram pessoas de um nome só. Muitos deles tiveram sua identidade alterada e ressignificada desde sua captura na África, passando pela travessia atlântica, até chegarem aos sertões de Pernambuco. Às vezes, nem pelo nome próprio (que receberam em português) essas pessoas eram chamadas. Despidos de sua humanidade, por vezes, eram meramente designados por “cabeças”, “peças”. Eram os marginais da sociedade, os párias, os ninguéns. Porém, nem toda desumanização é capaz de silenciar por completo suas histórias, suas individualidades. Nos próprios documentos da sociedade escravista aparecem vestígios sobre esses indivíduos.</p>

                <p> Sabe-se pouquíssimo a respeito dessas pessoas que faziam parte das escravarias de diversos proprietários de terras no interior de Pernambuco. Quem eram esses indivíduos escravizados? Como viviam? Qual a ligação deles com a África e com o Brasil? Esses são os principais questionamentos da pesquisa que nos leva a investigar a presença de escravos com suas histórias e culturas nas regiões interioranas de Pernambuco, durante os séculos XVIII e XIX.</p>

                <p> Nesse sentido, a partir da análise de fontes de pesquisa, procura-se municiar os alunos dos cursos de Marketing e Desenvolvimento de Sistemas da Escola Técnica Estadual Ministro Fernando Lyra na criação de um memorial digital que trará informações como o nome dos escravos, qualidades, idades, localidades e outros detalhes acerca dos cativos e do processo de escravidão. A partir da analise documental e da construção do banco de dados, os alunos desenvolverão competências e habilidades no que concerne aos procedimentos científicos e tecnológicos previstos na Base Nacional Comum Curricular (BNCC).</p>

