                <p> 
                    As fontes utilizadas consistem dos seguintes documentos: inventários post-mortem,
                    testamentos (localizados no Memorial da Justiça/Tribunal de Justiça e no Instituto Arqueológico,
                    Histórico e Geográfico Pernambucano) e manuscritos avulsos da capitania de Pernambuco (localizado
                    no Arquivo Histórico Ultramarino). Nesses tipos de documentos, por vezes, consegue-se saber as
                    condições e qualidades dos escravos, seu nome, idade aproximada e o lugar da África de onde provinham
                    (embora nem sempre fossem naturais do lugar de onde eram remetidos), entre outras informações.
                    Desse modo, salienta-se que a reconstrução da trajetória e do cotidiano dos escravos é uma
                    tentativa de identificá-los e situá-los na História. Sendo assim, baseamos-nos teórico-metodologicamente
                    no paradigma indiciário usado pelo historiador italiano Carlos Ginzburg, que nos permite enveredar nos
                    resíduos sobre dados de uma documentação fragmentada acerca de determinado tema. Além disso, tomamos a
                    micro-história como ângulo que nos permite analisar o âmbito local e regional da capitania/província de Pernambuco
                    em sintonia com os demais espaços do Brasil, nos séculos XVIII e XIX.
                </p>
