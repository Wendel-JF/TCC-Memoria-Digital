                <p> No que concerne ao ensino, o projeto de pesquisa contribuirá para pôr em prática o conjunto de competências e habilidades das Ciências
                    Humanas e Sociais Aplicadas, previstas na Base Nacional Comum Curricular (BNCC), dentre elas: </p>
                <ul class="list-group ">
                    <li class="list-group-item bg-dark text-white">
